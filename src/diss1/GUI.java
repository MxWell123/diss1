    public void vykresli(double vysledok1, double vysledok2) throws InterruptedException {
        graf1.addPoint(pocitadlo, vysledok1);
        graf2.addPoint(pocitadlo, vysledok2);
        pocitadlo++;
    }

    public void ukonci() {
        program.ukonciSimulaciu();
        Graf graf4 = new Graf();
        Graf graf3 = new Graf();
        graf3.createBarChart(pocetParkovacichMiest, program.dajVysledky("jedna"), 100, 200);
        graf4.createBarChart(pocetParkovacichMiest, program.dajVysledky("dva"), 700, 200);
        double[] vysledky = program.dajCelkoveVysledky();
        double factor = 1e5;
        double vysledok1 = Math.round(vysledky[0] * factor) / factor;
        double vysledok2 = Math.round(vysledky[1] * factor) / factor;
        strat1.setText(vysledok1 + "");
        strat2.setText(vysledok2 + "");
    }

